public class ForPractice05 {
    public static void main(String[] args) {


        for (var i = 0; i < 4; i++) {




                for (var j = 0; j <= i; j++) {
                    System.out.print("*");
                }
                for (var k = 0; 4 - i > k; k++) {
                    System.out.print(" ");
                }





            System.out.println("");
        }


        for (int i =5; i > 0; i--) {


                for (int j = 1; j <= i; j++) {
                    System.out.print("*");
                }
                for(var k=0; 5-i > k; k++) {
                    System.out.print(" ");
                }





            System.out.println("");
        }
//


    }
}
